class Switch_Prog{
    public static void main(String[] args){
        int result = 50;

        switch(result){
            case 60:
                System.out.println("You Result is 60%...");
                break;

            case 70:
                System.out.println("You Result is 70%...");
                break;

            default: 
                System.out.println("you are fail....");
        }
    }
}